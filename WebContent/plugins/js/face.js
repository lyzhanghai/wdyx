//replace face code to emotion resources
function replace_em(str){
	$.each(face,function(i){
		if(str.contains(i)){
			str = str.replace(i,'<img src="img/face/'+face[i]+'"/>');
		}
	});
	return str;
}

//replace auto
$(document).ready(function(){
	$.each($('p.message'),function(){
		$(this).html(replace_em($(this).html()));
	});
});